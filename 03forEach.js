// Looping Arrays: forEach
const movements = [200, 450, -400, 3000, -650, -130, 70, 1300];

// for (const movement of movements) {
for (const [i, movement] of movements.entries()) { // enteries method index and index pe ele deta ha 
  if (movement > 0) {
    console.log(`Movement ${i + 1}: You deposited ${movement}`);
  } else {
    console.log(`Movement ${i + 1}: You withdrew ${Math.abs(movement)}`);
  }
}
/* forEach method is a higher order function it requires a call back function and this will pass ele of array one by one  into function and it also passes curent ele index and whole array */
// continue and break statement does not work on forEach 
console.log('---- FOREACH ----');
movements.forEach( (mov,i) => {
  if (mov > 0) {
    console.log(`Movement ${i + 1}: You deposited ${mov}`);
  } else {
    console.log(`Movement ${i + 1}: You withdrew ${Math.abs(mov)}`);
  }
});
